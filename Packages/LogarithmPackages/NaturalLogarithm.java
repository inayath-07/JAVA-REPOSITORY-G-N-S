/*Logarithm having base e is known as Natural Logarithm
 *it's form loge^(n) = x;  which is equivalent to 
 * e^(x) = n;
*/
package LogarithmPackages;

public class NaturalLogarithm
{
    public double naturalLogarithm(int n)
    {
        double x =Math.log(n); //it is natural logarithm method
        return x;
    }
}